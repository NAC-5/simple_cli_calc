import math

def add(a, b):
    return a + b

def subtract(a, b):
    return a - b

def multiply(a, b):
    return a * b

def divide(a, b):
    return a / b

def power(a, b):
    return a ** b

def square(a):
    return a ** 2

def cube(a):
    return a ** 3

def square_root(a):
    return math.sqrt (a)

def cube_root(a):
    return a ** (1/3)

def modulus(a, b):
    return a % b

def factorial(a):
    return math.factorial(a)

def log10(a):
    return math.log10(a)

def natural_log(a):
    return math.log(a)

def sine(a):
    return math.sin(a)

def cosine(a):
    return math.cos(a)

def tangent(a):
    return math.tan(a)

def absolute(a):
    return abs(a)

def exponential(a):
    return math.exp(a)

def percentage(a, b):
    return (a / b) * 100

def pi(a):
    return math.pi * a

def e(a):
    return math.e * a
